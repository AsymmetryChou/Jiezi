class TestHamiltonianMatrix(unittest.TestCase):


    def test_TbHamiltonianOfArmChairCNT(self):
        cnt = CNT(n=4, m=0, Trepeat=3, nonideal=False)
        cnt.construct()
        H = hamilton(cnt, onsite=-0.28, hopping=-2.97)
        H.build_H()
        Hii = H.get_Hii()
        self.assertTrue(np.array_equal(Hii[1].get_value(), TestHamiltonianMatrix.expected_Hamiltonian40CNT))

    def test_TbHamiltonianOfZigzagCNT(self):
        cnt = CNT(n=5, m=5, Trepeat=3, nonideal=False)
        cnt.construct()
        H = hamilton(cnt, onsite=-0.28, hopping=-2.97)
        H.build_H()
        H.build_S(base_overlap=0.018)
        Hii = H.get_Hii()
        self.assertTrue(np.array_equal(Hii[1].get_value(), TestHamiltonianMatrix.expected_Hamiltonian55CNT))

    def test_TbHamiltonianOfRandomCNT(self):
        cnt = CNT(n=4, m=2, Trepeat=3, nonideal=False)
        cnt.construct()
        H = hamilton(cnt, onsite=-0.28, hopping=-2.97)
        H.build_H()
        Hii = H.get_Hii()
        self.assertTrue(np.array_equal(Hii[1].get_value(), TestHamiltonianMatrix.expected_Hamiltonian42CNT))

